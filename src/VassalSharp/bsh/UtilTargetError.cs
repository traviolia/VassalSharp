using System;
namespace bsh
{
	
	/// <summary>UtilTargetError is an error corresponding to a TargetError but thrown by a 
	/// utility or other class that does not have the caller context (Node) 
	/// available to it.  See UtilEvalError for an explanation of the difference
	/// between UtilEvalError and EvalError.
	/// <p>
	/// </summary>
	/// <seealso cref="UtilEvalError">
	/// </seealso>
	[Serializable]
	public class UtilTargetError:UtilEvalError
	{
		//UPGRADE_NOTE: Exception 'java.lang.Throwable' was converted to 'System.Exception' which has different behavior. "ms-help://MS.VSCC.v80/dv_commoner/local/redirect.htm?index='!DefaultContextWindowIndex'&keyword='jlca1100'"
		public System.Exception t;
		
		//UPGRADE_NOTE: Exception 'java.lang.Throwable' was converted to 'System.Exception' which has different behavior. "ms-help://MS.VSCC.v80/dv_commoner/local/redirect.htm?index='!DefaultContextWindowIndex'&keyword='jlca1100'"
		public UtilTargetError(System.String message, System.Exception t):base(message)
		{
			this.t = t;
		}
		
		//UPGRADE_NOTE: Exception 'java.lang.Throwable' was converted to 'System.Exception' which has different behavior. "ms-help://MS.VSCC.v80/dv_commoner/local/redirect.htm?index='!DefaultContextWindowIndex'&keyword='jlca1100'"
		public UtilTargetError(System.Exception t):this(null, t)
		{
		}
		
		/// <summary>Override toEvalError to throw TargetError type.</summary>
		public override EvalError toEvalError(System.String msg, SimpleNode node, CallStack callstack)
		{
			if (msg == null)
			{
				//UPGRADE_TODO: The equivalent in .NET for method 'java.lang.Throwable.getMessage' may return a different value. "ms-help://MS.VSCC.v80/dv_commoner/local/redirect.htm?index='!DefaultContextWindowIndex'&keyword='jlca1043'"
				msg = Message;
			}
			else
			{
				//UPGRADE_TODO: The equivalent in .NET for method 'java.lang.Throwable.getMessage' may return a different value. "ms-help://MS.VSCC.v80/dv_commoner/local/redirect.htm?index='!DefaultContextWindowIndex'&keyword='jlca1043'"
				msg = msg + ": " + Message;
			}
			
			return new TargetError(msg, t, node, callstack, false);
		}
	}
}